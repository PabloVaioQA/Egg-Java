
package Entidades;

/**
 *
 * @author vaiop
 */
public class Cadena {

    
    public String Cadena;
    public int longitud;


    public Cadena() {
    }

    public Cadena(String Cadena, int longitud) {
        this.Cadena = Cadena;
        this.longitud = longitud;
    }

    public String getCadena() {
        return Cadena;
    }

    public void setCadena(String Cadena) {
        this.Cadena = Cadena;
    }

    public int getLongitud() {
        return longitud;
    }

    public void setLongitud(int longitud) {
        this.longitud = longitud;
    }
        
       
        
    }
    

